    public class BasicMaths
    {
        public double Add(double num1, double num2)
        {
            return num1 + num2;
        }
        public double Substract(double num1, double num2)
        {
            return num1 - num2;
        }
        public double Mult(double num1, double num2)
        {
            return num1 * num2;
        }
    }
